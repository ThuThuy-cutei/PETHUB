-- ============================================================
-- GHI CHÚ VỀ CHUẨN HÓA (Normalization - 3NF):
-- 
-- 1NF: Mỗi cột chứa giá trị nguyên tử, không có nhóm lặp.
-- 2NF: Tất cả cột không khóa phụ thuộc hoàn toàn vào khóa chính.
-- 3NF: Không có phụ thuộc bắc cầu (transitive dependency).
--
-- Ví dụ: Bảng KHACH_HANG và THU_CUNG được tách riêng
-- thay vì gộp chung để tránh dư thừa dữ liệu (Anomalies):
--   - Insert Anomaly: Không thể thêm thú cưng nếu chưa có khách.
--   - Update Anomaly: Cập nhật SĐT khách phải sửa nhiều dòng.
--   - Delete Anomaly: Xóa thú cưng có thể mất thông tin khách.
-- ============================================================

-- Xóa bảng nếu đã tồn tại (theo thứ tự phụ thuộc)
BEGIN
    FOR t IN (SELECT table_name FROM user_tables WHERE table_name IN (
        'CAP_NHAT_THU_CUNG', 'CHI_TIET_DICH_VU', 'HOA_DON', 'DAT_PHONG', 
        'THU_CUNG', 'TAI_KHOAN', 'PHONG', 'LOAI_PHONG', 'DICH_VU', 
        'KHACH_HANG', 'NHAN_VIEN'
    )) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;
END;
/

-- Xóa sequence nếu đã tồn tại
BEGIN
    FOR s IN (SELECT sequence_name FROM user_sequences WHERE sequence_name IN (
        'SEQ_KHACH_HANG', 'SEQ_THU_CUNG', 'SEQ_LOAI_PHONG', 'SEQ_PHONG',
        'SEQ_DICH_VU', 'SEQ_NHAN_VIEN', 'SEQ_DAT_PHONG', 'SEQ_HOA_DON',
        'SEQ_CHI_TIET_DV', 'SEQ_TAI_KHOAN', 'SEQ_CAP_NHAT_TC'
    )) LOOP
        EXECUTE IMMEDIATE 'DROP SEQUENCE ' || s.sequence_name;
    END LOOP;
END;
/

-- ============================================================
-- SEQUENCES
